    fallback.textContent = "Recuerdo pendiente ❤️";
    fallback.hidden = true;

    image.onload = () => {
      image.hidden = false;
      fallback.hidden = true;
    };

    image.onerror = () => {
      image.hidden = true;
      fallback.hidden = false;
    };

    photoWrap.addEventListener("click", () => {
      if (!image.hidden) {
        openImageModal(image.src, momento.titulo);
      }
    });

    photoWrap.append(image, fallback);

