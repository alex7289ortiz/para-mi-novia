function createFloatingHearts(total = 26, className = "") {
  const fragment = document.createDocumentFragment();

  for (let index = 0; index < total; index += 1) {
    const heart = document.createElement("span");
    const size = 9 + Math.random() * 15;
    heart.className = `floating-heart ${className}`.trim();
    heart.style.setProperty("--left", `${Math.random() * 100}%`);
    heart.style.setProperty("--size", `${size}px`);
    heart.style.setProperty("--alpha", `${0.14 + Math.random() * 0.22}`);
    heart.style.setProperty("--duration", `${10 + Math.random() * 10}s`);
    heart.style.setProperty("--delay", `${Math.random() * -12}s`);
    fragment.appendChild(heart);
  }

  heartField.appendChild(fragment);
}

function createHeartBurst(amount = 10) {
  const centerX = window.innerWidth / 2;
  const centerY = window.innerHeight * 0.58;

  for (let index = 0; index < amount; index += 1) {
    const heart = document.createElement("span");
    const angle = (Math.PI * 2 * index) / amount;
    const distance = 54 + Math.random() * 58;
    heart.className = "burst-heart";
    heart.style.setProperty("--x", `${centerX + (Math.random() * 34 - 17)}px`);
    heart.style.setProperty("--y", `${centerY + (Math.random() * 28 - 14)}px`);
    heart.style.setProperty("--tx", `${Math.cos(angle) * distance}px`);
    heart.style.setProperty("--ty", `${Math.sin(angle) * distance - 62}px`);
    document.body.appendChild(heart);
    window.setTimeout(() => heart.remove(), 800);
  }
}

